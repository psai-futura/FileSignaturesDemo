using FileSignatures;

namespace FileSignaturesDemo.CustomFileFormats;

public abstract class Dxf : FileFormat
{
    protected Dxf(byte[] signature, string mediaType, string extension) : base(signature, mediaType, extension)
    {
    }

    protected Dxf(byte[] signature, string mediaType, string extension, int offset) : base(signature, mediaType, extension, offset)
    {
    }
}

